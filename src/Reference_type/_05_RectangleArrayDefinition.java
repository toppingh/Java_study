package Reference_type;

public class _05_RectangleArrayDefinition {
    public static void main(String[] args) {
        // 배열 선언1
        int[][] array1 = new int[3][4];
        int[][] array2;

        // 배열 선언2
        int array3[][] = new int[3][4];
        int array4[][];
        array4 = new int[3][4];

        // 배열 선언3
        int[] array5[] = new int[3][4];
        int[] array6[];
        array6 = new int[3][4];

        // 기본, 참조 자료형 배열 선언 방법
        boolean[][] array7 = new boolean[3][4];
        int[][] array8 = new int[3][4];
        double[][] array9 = new double[3][4];
        String[][] array10 = new String[3][4];
    }

}
